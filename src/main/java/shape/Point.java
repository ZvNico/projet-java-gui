package shape;

public class Point {
    private int posX, poxY;

    public Point(int posX, int poxY) {
        this.posX = posX;
        this.poxY = poxY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPoxY() {
        return poxY;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public void setPoxY(int poxY) {
        this.poxY = poxY;
    }
}
